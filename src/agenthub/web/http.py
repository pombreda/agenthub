EXCEPTION_CODES = (
    (ClassNotFound, 404),
    (MethodNotFound, 404),
    (NotPermitted, 405),
    (NotAuthorized, 401),
    (RequestTimeout, 408),
    (BadRequest, 400),
    (TypeError, 400),
)

EXCEPTIONS = tuple([E[0] for E in EXCEPTION_CODES])

def status(exception):
    for entry in EXCEPTION_CODES:
        if isinstance(exception, entry[0]):
            return entry[1]
    return 500
